function summarizeDataset(states, inputs, outlierCounter)
    fprintf("number of outliers: %d\n", outlierCounter)
    fprintf("Max x : %d m\n", max(states(:,:,1), [], 'all'))
    fprintf("Max y : %d m\n", max(states(:,:,2), [], 'all'))
    fprintf("Max theta : %d deg\n", max(states(:,:,3)*180/3.14, [], 'all'))
    fprintf("Max x_dot : %d m/s\n", max(states(:,:,4), [], 'all'))
    fprintf("Max y_dot : %d m/s\n", max(states(:,:,5), [], 'all'))
    fprintf("Max theta_dot : %d deg/s\n", max(states(:,:,6)*180/3.14, [], 'all'))
    
    fprintf("\n")
    fprintf("Mean x : %d m\n", mean(states(:,:,1), 'all'))
    fprintf("Mean y : %d m\n", mean(states(:,:,2), 'all'))
    fprintf("Mean theta : %d deg\n", mean(states(:,:,3)*180/3.14, 'all'))
    fprintf("Mean x_dot : %d m/s\n", mean(states(:,:,4), 'all'))
    fprintf("Mean y_dot : %d m/s\n", mean(states(:,:,5), 'all'))
    fprintf("Mean theta_dot : %d deg/s\n", mean(states(:,:,6)*180/3.14, 'all'))
    
    fprintf("\n")
    fprintf("Std x : %d m\n", std(states(:,:,1), 0, 'all'))
    fprintf("Std y : %d m\n", std(states(:,:,2), 0, 'all'))
    fprintf("Std theta : %d deg\n", std(states(:,:,3)*180/3.14, 0, 'all'))
    fprintf("Std x_dot : %d m/s\n", std(states(:,:,4), 0, 'all'))
    fprintf("Std y_dot : %d m/s\n", std(states(:,:,5), 0, 'all'))
    fprintf("Std theta_dot : %d deg/s\n", std(states(:,:,6)*180/3.14, 0, 'all'))

    fprintf("\n")
    deltaX = abs(states(:,1,1) - states(:,end,1));
    fprintf("Mean deltaX : %d m\n", mean(deltaX))

    fprintf("\n")
    fprintf("Mean F : %d m\n", mean(inputs(:,:,1), 'all'))
    fprintf("Std F : %d m\n", std(inputs(:,:,1), 0, 'all'))
    fprintf("Mean T : %d m\n", mean(inputs(:,:,2), 'all'))
    fprintf("Std T : %d m\n", std(inputs(:,:,2), 0, 'all'))
    fprintf("\n")